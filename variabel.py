# tipe data di python

# string yang ditandai dengan "" atau ''
# string bisa menyimpan karakter atau teks bahkan simbol dan emoji
Nama = 'ahmad soppian'
prodi = "sistem informasi"

# menampilkan tipe data pada variabel
print(type(Nama))
print(type(prodi))

# int atau interger
# tipe data yang berisi atau menyimpan sebuah angka
NIM = 19251391

# menampilkan tipe data pada variabel
print(type(NIM))

# float
# tipe data yang berisi atau menyimpan angka desimal atau pecahan
Tinggi = 166.2

# menampilkan tipe data pada variabel
print(type(Tinggi))

#  bolean hanya untuk 2 nilai yaitu True dan False
SudahMenikah = False
Mahasiswa = True

# menampilkan tipe data pada variabel
print(type(SudahMenikah))
